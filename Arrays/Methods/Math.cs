using System;
using System.Collections.Generic;
using System.Text;

namespace Methods
{
    public class Math
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Diff(int a, int b)
        {
            return a - b;
        }

        public int Multiply(int a, int b, int c, int d)
        {
            return a * b * c * d;
        }
        public int Divide(int a, int b)
        {
            return a / b;
        }

       
    }  


}
